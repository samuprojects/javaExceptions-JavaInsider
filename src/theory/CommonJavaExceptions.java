package theory;

@SuppressWarnings("ALL")
public class CommonJavaExceptions {

    /*

    Java famous Unchecked Exceptions

    - NullPointerException
    Exceção lançada quando tenta acessar um objeto a partir de uma referência nula, uma variável
    que não aponta para nenhum objeto no heap

    - ClassCastException
    Quando a tentativa de realização de casting explícito, exemplo, de uma classe para uma subclasse e
    o tipo de dado na memória não corresponde a subclasse que se deseja fazer o casting

    - ArrayIndexOutOfBoundsException
    Ao trabalhar com arrays para agrupar elementos na Java, estes são indexados de 0 até
    o tamanho do array -1, se tentar acessar um elemento fora desse limite lança essa exceção

    - ArithmeticException
    Quando realizado a tentativa de dividir um número inteiro por zero

    - NoSuchElementException
    Muito usado em coleções do Java quando um elemento não é encontrado, e pode ser usado também num contexto
    de busca onde você deseja lançar uma exceção caso não seja encontrado.

    LEMBRANDO QUE EXCEÇÕES DO TIPO UNCHECKED SÃO AQUELAS QUE NÃO DEVE TRATAR E SIM CORRIGIR A CAUSA,
    EVITAR QUE ELAS ACONTEÇAM

    Alguns exemplos práticos em App4

     */
}
